#include "../includes/ft_printf.h"

int	ft_putstr_mode(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		ft_putchar_mode(s[i], 0);
		i++;
	}
	return (ft_strlen(s));
}
